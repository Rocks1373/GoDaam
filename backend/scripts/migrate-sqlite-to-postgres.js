#!/usr/bin/env node
/**
 * Copy data from the main GoDam SQLite warehouse DB into PostgreSQL.
 *
 * Usage:
 *   SQLITE_PATH=./warehouse.db DATABASE_URL=postgresql://user:pass@localhost:5432/godam_db node scripts/migrate-sqlite-to-postgres.js
 *
 * Optional: --wipe-pg  (DROP SCHEMA public CASCADE; CREATE SCHEMA public;)
 *
 * Requires: npm packages `sqlite3` and `pg` (already backend dependencies).
 * Run AFTER creating an empty PostgreSQL database and user with DDL privileges.
 *
 * Does NOT delete the SQLite file.
 */
const path = require('path');
const backendRoot = path.join(__dirname, '..');
const repoRoot = path.join(backendRoot, '..');
require('dotenv').config({ path: path.join(backendRoot, '.env') });
require('dotenv').config({ path: path.join(repoRoot, '.env') });
const fs = require('fs');
const sqlite3 = require('sqlite3').verbose();
const { Client } = require('pg');
const { promisify } = require('util');

const SQLITE_PATH = path.resolve(process.env.SQLITE_PATH || process.env.DB_PATH || path.join(__dirname, '..', 'warehouse.db'));
const DATABASE_URL = String(process.env.DATABASE_URL || '').trim();
const WIPE = process.argv.includes('--wipe-pg');

if (!DATABASE_URL) {
  console.error('Set DATABASE_URL to the target PostgreSQL connection string.');
  process.exit(1);
}
if (!fs.existsSync(SQLITE_PATH)) {
  console.error('SQLite file not found:', SQLITE_PATH);
  process.exit(1);
}

function sqliteTypeToPg(decl, isPk) {
  const d = String(decl || '').toUpperCase();
  // BIGINT PRIMARY KEY alone does NOT auto-generate in PostgreSQL (breaks INSERTs that omit id).
  // BY DEFAULT allows bulk copy INSERTs that include explicit id values from SQLite.
  if (isPk && d.includes('INT')) return 'BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY';
  if (d.includes('INT')) return 'BIGINT';
  if (d.includes('REAL') || d.includes('FLOA') || d.includes('DOUB')) return 'DOUBLE PRECISION';
  if (d.includes('DATETIME') || d.includes('TIMESTAMP')) return 'TIMESTAMP';
  if (d.includes('DATE')) return 'DATE';
  if (d.includes('BLOB')) return 'BYTEA';
  return 'TEXT';
}

async function pgHasExistingData(client) {
  try {
    const r = await client.query(
      `SELECT COUNT(*)::int AS c FROM information_schema.tables
       WHERE table_schema = 'public' AND table_name = 'users'`
    );
    if (Number(r.rows[0]?.c) === 0) return false;
    const u = await client.query('SELECT COUNT(*)::int AS c FROM users');
    return Number(u.rows[0]?.c) > 0;
  } catch {
    return false;
  }
}

async function main() {
  const sqlite = new sqlite3.Database(SQLITE_PATH, sqlite3.OPEN_READONLY);
  const sAll = promisify(sqlite.all.bind(sqlite));
  const sClose = promisify(sqlite.close.bind(sqlite));

  const pg = new Client({ connectionString: DATABASE_URL });
  await pg.connect();

  const hasData = await pgHasExistingData(pg);
  if (hasData && !WIPE) {
    console.error(
      'REFUSED: Target PostgreSQL already has user data. This script DROPs tables per table and would destroy production data.\n' +
        '  • Normal VPS deploy (rsync --deploy) does NOT run this script.\n' +
        '  • To copy SQLite into an empty PG only, point DATABASE_URL at an empty database.\n' +
        '  • To replace an existing PG database entirely, pass --wipe-pg (destructive).'
    );
    await pg.end();
    await sClose();
    process.exit(1);
  }

  if (WIPE) {
    await pg.query('DROP SCHEMA IF EXISTS public CASCADE; CREATE SCHEMA public;');
    console.log('Wiped public schema on PostgreSQL target.');
  }

  const tables = await sAll(
    `SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name`
  );

  for (const { name } of tables) {
    const cols = await sAll(`PRAGMA table_info(${name})`);
    if (!cols.length) continue;

    const colDefs = cols.map((c) => {
      const pk = Number(c.pk) === 1;
      const t = sqliteTypeToPg(c.type, pk);
      const nn = Number(c.notnull) === 1 && !pk ? ' NOT NULL' : '';
      return `"${c.name}" ${t}${nn}`;
    });

    const createSql = `CREATE TABLE IF NOT EXISTS "${name}" (${colDefs.join(', ')})`;
    try {
      if (WIPE) {
        await pg.query(`DROP TABLE IF EXISTS "${name}" CASCADE`);
      }
      await pg.query(createSql);
    } catch (e) {
      console.warn(`[skip create] ${name}:`, e.message);
      continue;
    }

    const colNames = cols.map((c) => `"${c.name}"`).join(', ');
    const rows = await sAll(`SELECT * FROM "${name}"`);
    let n = 0;
    for (const row of rows || []) {
      const values = cols.map((c) => row[c.name]);
      const ph = cols.map((_, i) => `$${i + 1}`).join(', ');
      try {
        await pg.query(`INSERT INTO "${name}" (${colNames}) VALUES (${ph})`, values);
        n += 1;
      } catch (e) {
        console.warn(`[row] ${name} id=${row.id}:`, e.message);
      }
    }

    const sqCount = rows?.length || 0;
    const { rows: c2 } = await pg.query(`SELECT COUNT(*)::bigint AS c FROM "${name}"`);
    const pgCount = Number(c2[0]?.c) || 0;
    const ok = sqCount === pgCount ? 'OK' : 'MISMATCH';
    console.log(`${name.padEnd(36)} | sqlite ${String(sqCount).padStart(6)} | pg ${String(pgCount).padStart(6)} | ${ok}`);
  }

  await pg.end();
  await sClose();
  console.log('\nDone. Run backend with DB_TYPE=postgres after verifying counts. SQLite file unchanged.');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
